/*有4个圆塔，圆心分别为(2,2)、(-2,2)、(-2,-2)、(2,-2)，圆半径为1，
这四个塔的高度为10米，塔以外无建筑物。
输入任意点坐标，求该点的建筑高度（塔外高度为0）*/
#include<stdio.h>
int main()
{
	double x, y;
	if ((x - 2) * (x - 2) + (y - 2) * (y - 2) <= 1 || (x + 2) * (x + 2) + (y - 2) * (y - 2) <= 1 || (x + 2) * (x + 2) + (y + 2) * (y + 2) <= 1 || (x - 2) * (x - 2) + (y + 2) * (y + 2) <= 1)
		printf("10m");
	else
		printf("0m");
	return 0;
}